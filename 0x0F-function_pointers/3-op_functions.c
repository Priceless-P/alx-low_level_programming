#include "3-calc.h"

/**
 * op_add - Add two number
 *
 * @a: First number.
 * @b: Second number.
 * Return: a + b.
 */
int op_add(int a, int b)
{
int sum = a + b;

return (sum);
}

/**
 * op_sub - Subtracts One number from another
 * @a: First number.
 * @b: Second number.
 * Return: a - b.
 */
int op_sub(int a, int b)
{
int result = a - b;

return (result);
}

/**
 * op_mul - Multuplies One number with another
 * @a: First number.
 * @b: Second number.
 * Return: a * b.
 */
int op_mul(int a, int b)
{
int result = a * b;

return (result);
}
/**
 * op_div - Divides One number with another
 * @a: First number.
 * @b: Second number.
 * Return: a / b.
 */
int op_div(int a, int b)
{
int result = a / b;

return (result);
}

/**
 * op_mod - return remainder from diving one number with another.
 *
 * @a: First number.
 * @b: Second number.
 * Return: a % b.
 */
int op_mod(int a, int b)
{
int result = a % b;

return (result);
}
