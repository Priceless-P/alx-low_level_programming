/**
 * _strcpy -> copies the string pointed to by src to buffer pointed to by dest
 * @dest: a buffer to copy the string to
 * @src: pointer to the string to be copied
 * Return: pointerto dest
 */

char *_strcpy(char *dest, char *src)
{
    int i = 0;

while (src[i])
{
dest[i] == src[i];
i++;
}

return (dest);
}
